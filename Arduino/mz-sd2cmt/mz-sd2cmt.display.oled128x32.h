struct OLED128x32Display : DummyDisplay
{
	/// This function displays a text on the first line with a horizontal scrolling if necessary.
	static void scrollText(char* text, bool refresh_all = true)
	{
		const auto n = oled.strWidth(text);
		const auto w = oled.fontWidth();
		int i = min(oled_scroll_pos, n) / w;

		if (refresh_all)
		{
			oled.clear();
		}
		oled.set1X();
		oled.setCursor(12 - (oled_scroll_pos % w), 0);
		oled.print(text + i);
		if (!refresh_all)
		{
			oled.write(' ');
		}
		oled.setCursor(0, 0);
		oled.set2X();
		oled.print(entry_type ? "/LWB"[entry_type - 1] : '?');
		oled.set1X();
		if (refresh_all)
		{
			oled.setCursor(12, 1);
			if (entry_type > ENTRY_DIR)
				oled.print(ultrafast_enabled ? F("\x10: ultra-fast    ") : F("\x10: normal        "));
			if (entry_exists)
			{
				oled.setCursor(0, 3);
				oled.setInvertMode(true);
				entry.printFileSize(&oled);
				oled.write(' ');
				entry.printModifyDateTime(&oled);
				oled.setInvertMode(false);
			}
		}
	}

	static void setup()
	{
		oled.begin(&Adafruit128x32, OLED_I2C_ADDR);

		oled.setFont(AsciiFont5x7);
		oled.clear();

		oled.print(F("SD2MZCMT"));

		Serial.println("OLED 128x32");
	}

	static void displayCode(DisplayCode code)
	{
		static unsigned long start_time = millis();
		static byte          old_progress = 0;
		switch (code)
		{
		case DisplayCode::no_sdcard:
			oled.clear();
			oled.print(F("MZ-SD2CMT: No SD card"));
			break;

		case DisplayCode::set_entry_name:
			oled_scroll_time = millis() + SCROLL_WAIT;
			oled_scroll_pos = 0;
			scrollText(lfn);
			break;

		case DisplayCode::scroll_entry_name:
			if (millis() >= oled_scroll_time)
			{
				auto n = oled.strWidth(lfn) - oled.fontWidth();
				switch (oled_scroll_dir)
				{
				case +1:
					if (n - oled_scroll_pos > oled.displayWidth())
					{
						oled_scroll_time = millis() + OLED_SCROLL_SPEED;
						scrollText(lfn, false);
						++oled_scroll_pos;
					}
					else
					{
						oled_scroll_dir = -1;
						oled_scroll_time = millis() + OLED_SCROLL_WAIT;
						scrollText(lfn, false);
					}
					break;
				case -1:
					if (oled_scroll_pos > 0)
					{
						oled_scroll_time = millis() + OLED_SCROLL_SPEED;
						--oled_scroll_pos;
						scrollText(lfn, false);
					}
					else
					{
						oled_scroll_dir = +1;
						oled_scroll_time = millis() + OLED_SCROLL_WAIT;
					}
					break;
				default:
					if (n - oled_scroll_pos > oled.displayWidth())
					{
						oled_scroll_time = millis();
						oled_scroll_dir = +1;
					}
					break;
				}
			}
			break;

		case DisplayCode::startPlaying:
			oled.clear();
			oled_scroll_pos = 0;
			scrollText(lfn);
			oled.set2X();
			oled.setCursor(0, 2);
			oled.clearToEOL();
			oled.write('\x10');
			oled.set1X();
			oled.print(F("Playing... "));
			oled.setCursor(12, 3);
			oled.ssd1306WriteRam(255);
			for (auto i = 0; i < 5*16; ++i)
				oled.ssd1306WriteRam(129);
			oled.ssd1306WriteRam(255);
			start_time = millis();
			old_progress = 0;
			break;

		case DisplayCode::stopPlaying:
			if (true)
			{
				auto stop_time = millis();
				oled.set2X();
				oled.setCursor(0, 2);
				oled.clearToEOL();
				oled.write('\xFB');
				oled.set1X();
				oled.print(F("Done. "));
				oled.set2X();
				oled.print((stop_time - start_time + 999) / 1000);
				oled.set1X();
				oled.print(F("s."));
			}
			break;

		case DisplayCode::cancelPlaying:
			if (true)
			{
				auto cancel_time = millis();
				oled.set2X();
				oled.setCursor(0, 2);
				oled.clearToEOL();
				oled.write('X');
				oled.set1X();
				oled.print(F("Canceled. "));
				oled.set2X();
				oled.print((cancel_time - start_time + 999) / 1000);
				oled.set1X();
				oled.print(F("s."));
				oled.set1X();
			}
			break;

		case DisplayCode::pausePlaying:
			oled.set2X();
			oled.setCursor(0, 2);
			oled.write('\x13');
			oled.set1X();
			oled.print(F("Paus"));
			break;

		case DisplayCode::resumePlaying:
			oled.set2X();
			oled.setCursor(0, 2);
			oled.write('\x10');
			oled.set1X();
			oled.print(F("Play"));
			break;

		case DisplayCode::updateProgressBar:
			if (progress_size < 5 * 16)
			{
				auto new_progress = old_progress;
				old_progress = byte(progress_size);
				oled.setCursor(13 + new_progress, 3);
				while (new_progress < old_progress)
				{
					oled.ssd1306WriteRam(128+32+16+8+4+1);
					++new_progress;
				}
			}
			break;
		default:
			break;
		}
	}
};

